def avg_my(num1: int, num2: int) -> float:
    """
    Returns the average of two integers as a float.
    """
    return (num1 + num2) / 2


def headline_my(title: str) -> str:
    """
    Returns the input string in uppercase with an exclamation mark at the end.
    """
    return title.upper() + "!"


def list_concat(list1: list[int], list2: list[int], list3: list[int]) -> list[int]:
    """
    Returns a concatenated list of three integer lists.
    """
    return list1 + list2 + list3

